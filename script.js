// Функция для загрузки новостей
function loadNews(category) {
    const newsContainer = document.querySelector(`#${category} .news-container`);
    // Очищаем текущие новости перед загрузкой новых
    newsContainer.innerHTML = ''; 

    // Проверяем, есть ли новости для данной категории
    if (newsData[category]) {
        newsData[category].forEach(news => {
            const articleDiv = document.createElement('div');
            articleDiv.classList.add('news-article');
            articleDiv.innerHTML = `
                <h3>${news.title}</h3>
                <p>${news.content}</p>
            `;
            newsContainer.appendChild(articleDiv);
        });
    } else {
        newsContainer.innerHTML = `<p>Новостей в категории "${category}" пока нет.</p>`;
    }
}

// Загружаем мировые новости по умолчанию при загрузке страницы
document.addEventListener('DOMContentLoaded', () => {
    loadNews('world');

    // Добавляем обработчики событий для ссылок навигации
    document.querySelectorAll('nav ul li a').forEach(link => {
        link.addEventListener('click', (event) => {
            event.preventDefault(); // Отменяем стандартное поведение ссылки (переход)
            const category = event.target.dataset.category; // Получаем категорию из data-атрибута
            loadNews(category); // Загружаем новости для выбранной категории
        });
    });
});
